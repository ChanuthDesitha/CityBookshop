    public void fillFields() {
        
        String title = null;
        String author = null;
        String category = null;
        String price = null;
        String qty = null;
                
        try {
            String query = "SELECT book_id, title, author, category, price, qty FROM books WHERE book_id = " + us1.getBookId();
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();
            
            if(rs.next()) {
                bookId = Integer.parseInt(rs.getString("book_id"));
                title = rs.getString("title");
                author = rs.getString("author");
                category = rs.getString("category");
                price = rs.getString("price");
                qty = rs.getString("qty");
            }
        } catch(Exception e) {
            System.out.println(e);
        }
        
//        String bookID = bookId.toString();

        bookIdLabel.setText(String.valueOf(bookId));
        titleField.setText(title);
        authorField.setText(author);
        categoryDropdown.setSelectedItem(category);
        priceField.setText(price);
        qtyField.setText(qty);
    }

