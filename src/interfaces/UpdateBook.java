    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        
        String title = titleField.getText();
        String author = authorField.getText();
        int price = Integer.parseInt(priceField.getText());
        int qty = Integer.parseInt(qtyField.getText());
        String category = categoryDropdown.getSelectedItem().toString();
        
        int x = JOptionPane.showConfirmDialog(null, "Do you want to update this book?");
        
        
        us1.updateBook(x, bookId, title, author, price, qty, category);
        this.dispose();
    }//GEN-LAST:event_updateBtnActionPerformed

    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        int x = JOptionPane.showConfirmDialog(null, "Are you sure you want to remove this book?");

        if(x == 0) {
            us1.removeBook(bookId);
            this.dispose();
        }
    }//GEN-LAST:event_removeBtnActionPerformed

